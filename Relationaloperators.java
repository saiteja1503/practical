package oppspractical;
import java.util.Scanner;
public class Relationaloperators {
	public static void main(String[] args) {
	    
	    int a = 7, b = 11;

	    System.out.println("a is " + a + " and b is " + b);

	    System.out.println(a == b);  

	    System.out.println(a != b);  

	    System.out.println(a > b);  

	    System.out.println(a < b);  

	    System.out.println(a >= b);  

	    System.out.println(a <= b);  
	  }


}
