package oppspractical;
import java.util.Scanner;

public class Arithmeticoperators {
			  public static void main(String[] args) {
			    
			    int a = 12, b = 5;

			    System.out.println("a + b = " + (a + b));

			    System.out.println("a - b = " + (a - b));

			    System.out.println("a * b = " + (a * b));

			    System.out.println("a / b = " + (a / b));

			    System.out.println("a % b = " + (a % b));
			  }
			}